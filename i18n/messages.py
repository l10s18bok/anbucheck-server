# 지원 locale 목록
SUPPORTED_LOCALES = list(MESSAGES.keys())


def get_message(locale: str, key: str, **kwargs) -> str:
    """locale에 맞는 메시지 반환. 미지원 locale은 en_US fallback."""
    msgs = (
        MESSAGES.get(locale)
        or MESSAGES.get(locale.replace("-", "_"))
        or MESSAGES["en_US"]
    )
    template = msgs.get(key) or MESSAGES["en_US"].get(key, key)
    try:
        return template.format(**kwargs) if kwargs else template
    except (KeyError, IndexError):
        return template
